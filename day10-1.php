function cleanUpRow($row, $simplePairs) {
    $replacedString = $row;
    $hasPossible = true;
    while ($hasPossible) {
        $found = false;
        foreach ($simplePairs as $pair) {
            if (strpos($replacedString, $pair) !== false) {
                $replacedString = str_replace($pair, '', $replacedString);
                $found = true;
            }
        }

        if (!$found) {
            $hasPossible = false;
        }
    }

    return $replacedString;
}

function getFirstInvalidChar($row, $closings) {
    foreach(str_split($row) as $rChar) {
        if (in_array($rChar, $closings)) {
            return $rChar;
        }
    }

    return '';
}

$invalidRows = [];
foreach ($input as $signal) {
    $cleanedRow = cleanUpRow($signal, $simplePairs);
    $iChar = getFirstInvalidChar($cleanedRow, $closings);
    if ($iChar !== '') {
        $invalidRows[$signal] = $iChar;
    }
}

$sum = 0;
foreach ($invalidRows as $iChr) {
    $sum += $rewards[$iChr];
}

